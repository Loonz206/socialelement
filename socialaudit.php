   <h2>What is the Social Audit?</h2>

<p>The <b>Social Audit</b> an evaluation used to measure the overall health and popularity of a
business' online presence and is broken into the following four components:</p>
<ol>
<li>
<em><b>Online Search</b></em> - Online search indicates how easy it is for customers to find you on the
web. This evaluation is based on where your site ranks on major search engines, the
accuracy of your listings in directories and on review sites, and the number of online
channels that point back to your website.</li>

<li><em><b>Website</b></em> – Evaluation is based on how effectively your website turns website visitors
into customers, how quickly and easily people can access information, and how
compatible your website is with mobile phones.</li>

<li><em><b>Social Media</b></em> – Measures your business's social media activity and effectiveness. This
includes an analysis of the social media sites you are using, if those sites are being used
to their full potential, and how the general public is responding.</li>

<li><em><b>Review Sites</b></em> - Includes an analysis of how well your business is using review sites to
encourage customers to recommend your business to others, learn from customer
experiences, and to interact with clientele.</li>
</ol>

